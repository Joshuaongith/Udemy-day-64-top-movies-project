# ---------------------------------------------------------------------------
# ENVIRONMENT SETUP & CONFIGURATION
# ---------------------------------------------------------------------------
# load_dotenv() reads the hidden .env file so we can securely access API keys
# without hardcoding them into the script (which is a major security risk).
load_dotenv()

# Retrieve API credentials and secret keys from the environment variables
bearer_token = os.getenv("API_READ_ACCESS_TOKEN")
api_endpoint = os.getenv("API_ENDPOINT")

# Pre-package the Authorization header for TMDB API requests
Authentication_headers = {"Authorization": f"Bearer {bearer_token}"}

# Initialize the core Flask application
app = Flask(__name__)
app.config['SECRET_KEY'] = os.getenv("SECRET_KEY")

# Wrap the Flask app in Bootstrap5 to automatically style our HTML and WTForms
Bootstrap5(app)

# ---------------------------------------------------------------------------
# DATABASE ARCHITECTURE
# ---------------------------------------------------------------------------
# Configure the SQLite database file path relative to the project directory
app.config['SQLALCHEMY_DATABASE_URI'] = 'sqlite:///Top-10-Movies.db'


# Define the declarative base class which all our database models will inherit from
class Base(DeclarativeBase):
    pass


# Initialize the SQLAlchemy extension using our Base class and bind it to the Flask app
db = SQLAlchemy(model_class=Base)
db.init_app(app)


# --- THE MOVIE TABLE MODEL ---
class Movie(db.Model):
    """
    Represents the 'movie' table in the SQLite database.
    Each property defines a specific column type and constraint.
    """
    # Primary Key: A unique integer auto-generated by SQLite for every new row
    id: Mapped[int] = mapped_column(Integer, primary_key=True)

    title: Mapped[str] = mapped_column(String(250), nullable=False)
    year: Mapped[int] = mapped_column(Integer, nullable=False)
    description: Mapped[str] = mapped_column(String(250), nullable=False)
    rating: Mapped[float] = mapped_column(Float, nullable=False)

    # ranking is nullable=True so we can initialize movies with 'None'
    # before our sorting algorithm dynamically calculates their true rank.
    ranking: Mapped[int] = mapped_column(Integer, nullable=True)

    review: Mapped[str] = mapped_column(String(250), nullable=False)
    img_url: Mapped[str] = mapped_column(String(500), nullable=False)


# Ensure the database and tables actually exist before the app runs requests.
# The app_context() is required because db.create_all() needs to know which Flask app it belongs to.
with app.app_context():
    db.create_all()


# ---------------------------------------------------------------------------
# WTFORMS DEFINITIONS
# ---------------------------------------------------------------------------
class RateMovieForm(FlaskForm):
    """Form used on the Edit page to update a movie's rating and review."""
    # DataRequired() ensures the user cannot submit an empty string/null value
    rating = FloatField("Your Rating Out of 10 (e.g. 7.5)", validators=[DataRequired()])
    review = StringField("Your Review", validators=[DataRequired()])
    submit = SubmitField("Done")


class AddMovieForm(FlaskForm):
    """Form used on the Add page to search for a movie title via the TMDB API."""
    title = StringField("Movie Title", validators=[DataRequired()])
    submit = SubmitField("Add Movie")


